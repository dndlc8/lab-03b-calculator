export function add (numberOne, numberTwo){
    return (Number(numberOne) + Number(numberTwo))
};

export function sub (numberOne, numberTwo){
    return (Number(numberOne) - Number(numberTwo))
};

export function multi (numberOne, numberTwo){
    return (Number(numberOne) * Number(numberTwo))
};

export function div (numberOne, numberTwo){
    return (Number(numberOne) / Number(numberTwo))
};